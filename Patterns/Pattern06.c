#include <stdio.h>
#define MIN(a, b) ((a) > (b) ? (b) : (a))

int main()
{
    int n = 5, size = n + (n - 1);
    for(int i = 0; i < size; i++) {
        for(int j = 0; j < size; j++) {
            int ans = n - MIN(MIN(i, j), MIN(size - 1 - i, size - 1 - j));
            printf("%d ", ans);
        }
        printf("\n");
    }

    return 0;
}
