#include <stdio.h>

int main()
{
    int n = 5, odd = (n * 2) - 1, even = 1;
    for(int i = 1; i <= n; i++, odd -= 2, even += 2) {
        int ans = i;
        printf("%4d", ans);
        for(int j = 1; j < n; j++) {
            if(j % 2) {
                ans += odd;
            } else {
                ans += even;
            }
            printf("%4d", ans);
        }
        printf("\n");
    }

    return 0;
}
