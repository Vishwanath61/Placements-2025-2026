/******************************************************************************

        1
      2 1
    3 2 1
  4 3 2 1
5 4 3 2 1

*******************************************************************************/

#include <stdio.h>

int main()
{
    int n = 5;
    for(int i = 1; i <= n; i++) {
        for(int j = i; j < n; j++) {
            printf("  ");
        }
        for(int k = i; k >= 1; k--) {
            printf("%d ", k);
        }
        printf("\n");
    }

    return 0;
}
