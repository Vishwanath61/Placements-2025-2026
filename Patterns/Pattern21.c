#include <stdio.h>

int main()
{
    int n = 3, mid = (n / 2), seq = 1;
    
    for(int i = 1; i <= n; i++) {
        int ans = i;
        for(int j = 1; j <= seq; j++) {
            printf("%3d", ans);
            ans += (n - j);
        }
        seq += (i <= mid) ? 1 : -1;
        printf("\n");
    }

    return 0;
}
